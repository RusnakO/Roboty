def speed(v, t):
    return(v*t)%109
v=int(input("Введіть значення швидкості: "))
t=int(input("Введіть значення часу: "))
pos=speed(v,t)
if v>0:
    print(f"Байкер зупиниться на {pos} км позначки в позитивному напрямку")
elif v == 0:
    print("Байкер не рухається")
else:
    print(f"Байкер зупиниться на {109 - pos} км позначки в негативному напрямку")
    
     
